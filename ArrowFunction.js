// sum function 

function sum(a,b){
    return a+b;
}

// multiplication function 

function mul(a,b){
    return a*b;
}
// arrow function of sum 
const arrowSum=(a,b)=>{
    console.log(a+b);

}
arrowSum(3,4);

//arrow function of multiplication 

const arrowMul = (a,b)=>{
    console.log(a*b);
}

arrowMul(4,5);